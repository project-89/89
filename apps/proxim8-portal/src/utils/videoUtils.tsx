import React from "react";

/**
 * Get status color based on video status
 */
export const getStatusColor = (status: string): string => {
  switch (status) {
    case "COMPLETED":
      return "text-green-400";
    case "PROCESSING":
      return "text-yellow-400";
    case "PENDING":
      return "text-blue-400";
    case "FAILED":
      return "text-red-400";
    default:
      return "text-gray-400";
  }
};

